# Строковый тип
x = "Hello World"  # str
print(type(x))
print(x)

# Целочисленный тип
x = 20  # int
print(type(x))
print(x)

# Число с плавающей точкой
x = 20.5  # float
print(type(x))
print(x)

# Комплексное число
x = 1j  # complex
print(type(x))
print(x)

# Список
x = ["apple", "banana", "cherry"]  # list
print(type(x))
print(x)

# Кортеж
x = ("apple", "banana", "cherry")  # tuple
print(type(x))
print(x)

# Диапазон
x = range(6)  # range
print(type(x))
print(x)

# Словарь
x = {"name": "John", "age": 36}  # dict
print(type(x))
print(x)

# Множество
x = {"apple", "banana", "cherry"}  # set
print(type(x))
print(x)

# Невозможное для изменения множество
x = frozenset({"apple", "banana", "cherry"})  # frozenset
print(type(x))
print(x)

# Логический тип
x = True  # bool
print(type(x))
print(x)

# Байт
x = b"Hello"  # bytes
print(type(x))
print(x)

# Массив байтов
x = bytearray(5)  # bytearray
print(type(x))
print(x)

# Объект memoryview
x = memoryview(bytes(5))  # memoryview
print(type(x))
print(x)

# Тип None
x = None  # NoneType
print(type(x))
print(x)

